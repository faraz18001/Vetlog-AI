@router.post("/chat/", response_model=ChatResponse)
def chat_endpoint(payload: ChatRequest, db: Session = Depends(get_session)):
    agent = get_current_agent()
    callback = HallucinationCallback()
    config = {
        "configurable": {"thread_id": payload.thread_id},
        "metadata": {
            "user_id": payload.user_id,
            "thread_id": payload.thread_id,
        },
        "callbacks": [callback],
    }
    result = agent.invoke({"messages": [("user", payload.message)]}, config=config)
    messages = result["messages"]
    last_message = messages[-1]

    response_text = extract_content(last_message)
    usage = extract_usage(messages)
    accumulate_usage(usage)
    if usage:
        usage_dump = usage.model_dump()
    else:
        usage_dump = None

    if payload.user_id is not None:
        thread_name = payload.message[:80]
        logging_turns(
            db,
            user_id=payload.user_id,
            thread_id=payload.thread_id,
            thread_name=thread_name,
            role="user",
            content=payload.message,
        )
        logging_turns(
            db,
            user_id=payload.user_id,
            thread_id=payload.thread_id,
            thread_name=thread_name,
            role="assistant",
            content=response_text,
        )
    _write_trace_log(
        payload.thread_id,
        payload.message,
        messages,
        usage_dump,
    )
    report_path = find_report_path(messages)
    table_path = find_table_path(messages)
    steps = extract_steps(messages)

    return ChatResponse(
        response=response_text,
        thread_id=payload.thread_id,
        usage=usage,
        report_path=report_path,
        table_path=table_path,
        steps=steps,
        eval_warnings=callback.get_warnings(),
    )


@router.post("/chat/stream/")
async def chat_stream(payload: ChatRequest, db: Session = Depends(get_session)):
    agent = get_current_agent()
    callback = HallucinationCallback()
    config = {
        "configurable": {"thread_id": payload.thread_id},
        "metadata": {
            "user_id": payload.user_id,
            "thread_id": payload.thread_id,
        },
        "callbacks": [callback],
    }

    async def event_generator():
        report_path = None
        table_path = None
        total_input = 0
        total_output = 0
        trace_lines: list[str] = []  # collected for trace log
        response_text: list[str] = []

        try:
            async for event in agent.astream_events(
                {"messages": [("user", payload.message)]},
                config=config,
                version="v2",
            ):
                kind = event["event"]

                if kind == "on_tool_start":
                    tool_name = event.get("name", "")
                    if tool_name == "execute_sql_query":
                        tool_input = event.get("data", {}).get("input", {}) or {}
                        query = tool_input.get("query", "")
                        trace_lines.append(
                            f"### execute_sql_query\n\n```sql\n{query}\n```\n\n"
                        )
                        yield _sse(
                            {
                                "type": "step",
                                "label": "Querying database",
                                "detail": query[:120],
                            }
                        )
                    elif tool_name == "query_to_inline_table":
                        tool_input = event.get("data", {}).get("input", {}) or {}
                        query = tool_input.get("query", "")
                        trace_lines.append(
                            f"### query_to_inline_table\n\n```sql\n{query}\n```\n\n"
                        )
                        yield _sse(
                            {
                                "type": "step",
                                "label": "Querying full dataset",
                                "detail": query[:120],
                            }
                        )
                    elif tool_name == "generate_static_report":
                        tool_input = event.get("data", {}).get("input", {}) or {}
                        title = tool_input.get("title", "")
                        trace_lines.append(f"### generate_static_report\n\n{title}\n\n")
                        yield _sse(
                            {
                                "type": "step",
                                "label": "Generating report",
                                "detail": title,
                            }
                        )
                    elif tool_name == "generate_dynamic_report":
                        tool_input = event.get("data", {}).get("input", {}) or {}
                        title = tool_input.get("title", "")
                        trace_lines.append(
                            f"### generate_dynamic_report\n\n{title}\n\n"
                        )
                        yield _sse(
                            {
                                "type": "step",
                                "label": "Generating report",
                                "detail": title,
                            }
                        )
                    else:
                        trace_lines.append(f"### {tool_name}\n\n")
                        yield _sse(
                            {
                                "type": "step",
                                "label": f"Running {tool_name}",
                                "detail": "",
                            }
                        )

                elif kind == "on_tool_end":
                    output = _tool_output_str(event.get("data", {}).get("output", ""))

                    if output.startswith("Error:"):
                        if output.startswith("Error: "):
                            err_detail = output[7:]
                        else:
                            err_detail = ""
                        trace_lines.append(f"**Error:** {err_detail[:200]}\n\n")
                        yield _sse(
                            {
                                "type": "step",
                                "label": "Query failed",
                                "detail": err_detail[:120],
                            }
                        )
                    elif output.startswith("reports/") and "Rows:" in output:
                        table_path = output.splitlines()[0].strip()
                        split_lines = output.splitlines()
                        if len(split_lines) > 1:
                            rows_info = split_lines[1]
                        else:
                            rows_info = ""
                        trace_lines.append(
                            "Saved: `" + table_path + "`\n\nRows: " + rows_info + "\n\n"
                        )
                        yield _sse(
                            {"type": "step", "label": "Table saved", "detail": ""}
                        )
                    elif output.startswith("reports/"):
                        report_path = output
                        trace_lines.append(f"Saved: `{output}`\n\n")
                        yield _sse(
                            {"type": "step", "label": "Report saved", "detail": ""}
                        )
                    elif output == "No results found.":
                        trace_lines.append(f"0 rows returned\n\n")
                        yield _sse(
                            {"type": "step", "label": "0 rows returned", "detail": ""}
                        )
                    else:
                        lines = []
                        for l in output.strip().splitlines():
                            if l.strip():
                                lines.append(l)
                        row_count = max(0, len(lines) - 1)
                        row_label = "row"
                        if row_count != 1:
                            row_label = "rows"
                        label = str(row_count) + " " + row_label + " returned"
                        trace_lines.append(
                            str(row_count) + " " + row_label + " returned\n\n"
                        )
                        yield _sse({"type": "step", "label": label, "detail": ""})

                elif kind == "on_chat_model_stream":
                    chunk = event.get("data", {}).get("chunk")
                    if chunk is None:
                        continue
                    # Skip chunks that only carry tool-call deltas — we want text.
                    if getattr(chunk, "tool_call_chunks", None):
                        continue
                    for t in _iter_chunk_texts(getattr(chunk, "content", "")):
                        response_text.append(t)
                        yield _sse({"type": "chunk", "text": t})

                elif kind == "on_chat_model_end":
                    meta = event.get("data", {}).get("output")
                    if meta:
                        usage_meta = getattr(meta, "usage_metadata", None)
                    else:
                        usage_meta = None
                    if usage_meta:
                        total_input += usage_meta.get("input_tokens", 0)
                        total_output += usage_meta.get("output_tokens", 0)

        except Exception as exc:
            trace_text = "".join(response_text).strip()
            try:
                _write_trace_from_parts(
                    thread_id=payload.thread_id,
                    user_message=payload.message,
                    trace_lines=trace_lines,
                    response_text=trace_text,
                    error=traceback.format_exc(),
                )
            except Exception:
                pass
            yield _sse({"type": "error", "message": str(exc)})
            return

        input_cost = (total_input / 1000) * INPUT_TOKEN_PRICE_PER_1K
        output_cost = (total_output / 1000) * OUTPUT_TOKEN_PRICE_PER_1K
        usage = {
            "input_tokens": total_input,
            "output_tokens": total_output,
            "total_tokens": total_input + total_output,
            "cost_usd": round(input_cost + output_cost, 6),
        }
        accumulate_usage(TokenUsage(**usage))

        trace_text = "".join(response_text).strip()
        _write_trace_from_parts(
            thread_id=payload.thread_id,
            user_message=payload.message,
            trace_lines=trace_lines,
            response_text=trace_text,
            usage=usage,
        )

        if payload.user_id is not None:
            thread_name = payload.message[:80]
            logging_turns(
                db,
                user_id=payload.user_id,
                thread_id=payload.thread_id,
                thread_name=thread_name,
                role="user",
                content=payload.message,
            )
            logging_turns(
                db,
                user_id=payload.user_id,
                thread_id=payload.thread_id,
                thread_name=thread_name,
                role="assistant",
                content=trace_text,
            )

        for warning in callback.get_warnings():
            yield _sse({"type": "eval_warning", "message": warning})

        yield _sse(
            {
                "type": "done",
                "thread_id": payload.thread_id,
                "report_path": report_path,
                "table_path": table_path,
                "usage": usage,
            }
        )

    return StreamingResponse(
        event_generator(),
        media_type="text/event-stream",
        headers={
            "Cache-Control": "no-cache",
            "X-Accel-Buffering": "no",
            "Connection": "keep-alive",
        },
    )


@router.get("/usage/", response_model=UsageStats)
def usage_stats():
    pricing_is_configured = (
        INPUT_TOKEN_PRICE_PER_1K > 0 or OUTPUT_TOKEN_PRICE_PER_1K > 0
    )
    return UsageStats(
        **_usage,
        pricing_configured=pricing_is_configured,
    )
